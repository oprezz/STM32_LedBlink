/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	int counter = 0;
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	/* A Timert(3-ast) alap(base, nem pwm, nem encoder stb..) módban elindítjuk, úgy, hogy ha eléri a kívánt értéket, 
	 * akkor interruptot generál. */
  HAL_TIM_Base_Start_IT(&htim3);		
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	/* A while(1) törzsében futó kód periódikusan meghívódik. Futását csak a külso interruptok szakítják meg.
	 * A kód célja, hogy gombnyomásra a led villogás frekvenciája megváltozzon, ehhez a led villogását idozíto
	 * Timer periódusidejét átállítjuk és a Timert újraindítjuk.
	*/
  while (1)
  {

  /* USER CODE END WHILE */
	/* Beolvassuk a nyomógombot, ami a PC13-as pin-en van. Ha lenyomjuk a nyomógombot,
	 * akkor LOW (0) logikai szint lesz a PC13-on.
	 * Alapvetoen gombokat nem így kezelünk, hanem célszeru IT-ként konfigurálni oket,
	 * ezzel elkerühetjük a folyamatos polling overheadet (hogy mindig ránézünk az értékére). 
	 * Továbbá a jelen esetben használt HAL_Delay() fv. használata is inkább csak fejlesztés közben használatos,
	 * hiszen ekkor a uC várakozik. Az IT-os gomb "olvasással" erre nincs szükség, itt pergésmentesítés miatt volt használva.
	 * A counterek azért számlálnak 2-vel, mert a benne lévo kódot csak 1-szer szeretnénk, hogy lefusson
	 * (vsz. erre is gyorsan található szebb megoldás). */
	if ( HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == 0)
	{
		counter++;
		HAL_Delay(100);
	}
	
	if ( counter == 0)
	{
		htim3.Init.Period = 10000;
		HAL_TIM_Base_Init(&htim3);
		HAL_TIM_Base_Start_IT(&htim3);
		counter++;
	}
	
  if ( counter == 2)
	{
		htim3.Init.Period = 1000;
		HAL_TIM_Base_Init(&htim3);
		HAL_TIM_Base_Start_IT(&htim3);
		counter++;
	}
  
	if ( counter == 4)
	{
		htim3.Init.Period = 30000;
		HAL_TIM_Base_Init(&htim3);
		HAL_TIM_Base_Start_IT(&htim3);
		counter++;
	}
	
	if ( counter == 6)
	{
		counter = 0;
	}
	
		
	/* USER CODE BEGIN 3 */
	
  }
  /* USER CODE END 3 */

}

